    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        
        if(!txtExistencias.getText().isEmpty()){
            Connection con = Conexion.getConexion();
        
            JasperReport reporte = null;
            String path = "src\\app\\Productos.jasper";

            Map parametro = new HashMap();
            parametro.put("pam_existencias", txtExistencias.getText());

            try {
                reporte = (JasperReport) JRLoader.loadObjectFromFile(path);

                JasperPrint jprint = JasperFillManager.fillReport(path, parametro, con);

                JasperViewer view = new JasperViewer(jprint, false);
                view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
                view.setVisible(true);

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Ingrese par치metros v치lidos.\n" + ex.toString());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese par치metros v치lidos.");
        }
        
        
    }//GEN-LAST:event_btnGenerarActionPerformed

